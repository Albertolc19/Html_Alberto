public class Main {
    public static void main(String[] args) {
      double impuesto= (2.2*1.0)+5.0;
      System.out.println("el impuesto es igual a "+impuesto);
      int impuesto2= (12/2)-8;
      System.out.println("el impuesto2 es igual a "+impuesto2);
      int cociente= (int) 16/3;
      System.out.println("el cociente es igual a "+cociente);
      double resto= 16%3;
      System.out.println("el resto es igual a "+resto);
      double cociente_decimal= 16.0/3;
      System.out.println("el resto es igual a "+cociente_decimal);
      byte nueve= 9;
      int postincremento= nueve++;
      System.out.println("el resultado es igual a "+postincremento);
      int preincremento= ++nueve;
      System.out.println("el resultado es igual a "+preincremento);
      int postdecremento= nueve--;
      System.out.println("el resultado es igual a "+postdecremento);
      int predecremento= --nueve;
      System.out.println("el resultado es igual a "+predecremento);


    }
}